int main (void)
{
    Pin_ConfigType pinList[] = {PA5,PC13};
    Port_ConfigType portCfg{pinList,2};
    Port BSP(&portCfg);
    SysTick TimeBase(&SYSTICK_CONFIG);
    while(TRUE)
    {
            BSP.TooglePin(GREEN_LED);
            TimeBase.Delay(500);
    }
    return EXIT_SUCCESS;
}

