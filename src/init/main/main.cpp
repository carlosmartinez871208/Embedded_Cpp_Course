int main (void)
{
    /* Enable GPIOA peripheral */
    RCC rcc;
    GPIO porta(PORTA);
    rcc.EnableGPIO(RCC_AHBENR_GPIOA_ENABLE);
    porta.InitPinMode(PIN5_OUTPUT_MODE);
    while(TRUE)
    {
        porta.TogglePin(PIN5);
        for (uint32_t i = 0; i < 1000000; i++);
    }
    return EXIT_SUCCESS;
}

